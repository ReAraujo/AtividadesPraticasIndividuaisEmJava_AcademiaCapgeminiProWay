public class Calculadora {

    static int somar(int n1, int n2){
        int soma = n1 + n2;
        return soma;
    }

    static int subtrair(int n1, int n2){
        int subtracao = n1 - n2;
        return subtracao;
    }

    static int multiplicar(int n1, int n2){
        int multiplicacao = n1 * n2;
        return multiplicacao;
    }

    static int dividir(int n1, int n2){
        int divisao = n1 / n2;
        return divisao;
    }
    
}
